public class Personel {
    public String isim;
    public String soyIsim;
    public int yas;
    public String departman;
    public int maas;

    public Personel(String isim, String soyIsim, int yas, String departman, int maas){
        this.isim=isim;
        this.soyIsim=soyIsim;
        this.yas=yas;
        this.departman=departman;
        this.maas=maas;
    }
}
